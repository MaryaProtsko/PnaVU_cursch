QImage createImageFromMatrix(const int matrix[MATRIX_SIZE][MATRIX_SIZE])
{
    QImage image(MATRIX_SIZE * SQUARE_SIZE, MATRIX_SIZE * SQUARE_SIZE, QImage::Format_Grayscale8);
    image.fill(Qt::white);

    for (int y = 0; y < MATRIX_SIZE; ++y)
    {
        for (int x = 0; x < MATRIX_SIZE; ++x)
        {
            if (matrix[y][x] == 1)
            {
                for (int dy = 0; dy < SQUARE_SIZE; ++dy)
                {
                    for (int dx = 0; dx < SQUARE_SIZE; ++dx)
                    {
                        if (x * SQUARE_SIZE + dx < image.width() && y * SQUARE_SIZE + dy < image.height())
                        {
                            image.setPixel(x * SQUARE_SIZE + dx, y * SQUARE_SIZE + dy, qRgb(0, 0, 0));
                        }
                    }
                }
            }
            if (matrix[y][x] == 2)
            {
                for (int dy = 0; dy < SQUARE_SIZE; ++dy)
                {
                    for (int dx = 0; dx < SQUARE_SIZE; ++dx)
                    {
                        if (x * SQUARE_SIZE + dx < image.width() && y * SQUARE_SIZE + dy < image.height())
                        {
                            image.setPixel(x * SQUARE_SIZE + dx, y * SQUARE_SIZE + dy, qRgb(255, 0, 255));
                        }
                    }
                }
            }
        }
    }
    return image;
}

