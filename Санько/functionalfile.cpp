void NewString::AddServInf()
{
    int len = binaryStream.size()/8;

    for(int i = 0; i < 16; i++)
    {
        if (len > 0)
        {
           binaryStream.prepend((len % 2 > 0) ? '1' : '0');
            len /= 2;
        }
        else
        {
            binaryStream.prepend('0');
        }
    }

    binaryStream.prepend(CodMet);
}
